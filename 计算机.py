import time
import os
import sys

皮肤 = {
    "主题颜色": "\033[95m",
    "副颜色": "\033[94m",
    "重置": "\033[0m"
}

def 清屏():
    os.system("clear")

def 打字效果(文字, 延迟=0.03):
    for i in 文字:
        print(i, end='', flush=True)
        time.sleep(延迟)
    print()

def 彩蛋():
    清屏()
    打字效果(f"{皮肤['主题颜色']}\n💖 我爱刘婧仪 💖", 0.05)
    打字效果("她是我最珍贵的宝贝 🌸\n", 0.05)
    打字效果("感谢命运让我遇见她。", 0.05)
    打字效果("刘婧仪，我会永远在你身边。\n\n", 0.05)
    打字效果(f"{皮肤['重置']}返回主菜单请输入回车...")
    input()

def 计算():
    清屏()
    打字效果(f"{皮肤['主题颜色']}╭────────────────────────────╮")
    打字效果("│     🌸 少女计算器终端版 🌸       │")
    打字效果("╰────────────────────────────╯" + 皮肤['重置'])
    try:
        表达式 = input("请输入算式（例如 2+2）：\n➤ ")
        if 表达式.strip() == '520' or eval(表达式) == 520:
            彩蛋()
        else:
            结果 = eval(表达式)
            print(f"\n结果是：{皮肤['副颜色']}{结果}{皮肤['重置']}")
    except Exception as e:
        print(f"发生错误：{e}")
    input("\n按回车返回菜单...")

while True:
    清屏()
    print(f"{皮肤['主题颜色']}╭────────────────────────────╮")
    print("│        🌸 少女计算器菜单 🌸       │")
    print("╰────────────────────────────╯")
    print("1. 进入计算器")
    print("2. 退出")
    print("══════════════════════════════")
    print(f"{皮肤['重置']}")
    选择 = input("请输入选项：")
    if 选择 == '1':
        计算()
    elif 选择 == '2':
        print("再见喵~")
        break
    else:
        print("请输入正确选项~")
        time.sleep(1)
