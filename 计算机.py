import time
import os

# 皮肤样式（粉红主题）
粉红 = "\033[95m"
天蓝 = "\033[94m"
重置 = "\033[0m"

def 清屏():
    os.system("clear")

def 打字(内容, 延迟=0.02):
    for 字 in 内容:
        print(字, end='', flush=True)
        time.sleep(延迟)
    print()

def 彩蛋():
    清屏()
    打字(f"{粉红}💗 我爱刘婧仪 💗", 0.05)
    打字("她是我最珍贵的宝贝 🌸", 0.05)
    打字("感谢命运让我遇见她。", 0.05)
    打字("刘婧仪，我会永远在你身边。", 0.05)
    打字(f"{重置}\n按回车返回菜单...", 0.02)
    input()

def 计算器():
    清屏()
    打字(f"{粉红}╭────────────────────────────╮")
    打字("│       🌸 少女计算器终端版 🌸       │")
    打字("╰────────────────────────────╯" + 重置)
    表达式 = input("请输入算式（例如 4+4）：\n➤ ")

    try:
        if 表达式.strip() == '520':
            彩蛋()
        else:
            结果 = eval(表达式)
            if 结果 == 520:
                彩蛋()
            else:
                print(f"\n{天蓝}结果是：{结果}{重置}")
    except:
        print(f"{天蓝}出错了，请检查算式格式{重置}")
    input("\n按回车返回菜单...")

def 菜单():
    while True:
        清屏()
        print(f"{粉红}╭────────────────────────────╮")
        print("│        🌸 少女计算器菜单 🌸        │")
        print("╰────────────────────────────╯")
        print("1. 进入计算器")
        print("2. 退出")
        print("══════════════════════════════")
        print(重置)
        选择 = input("请输入选项：")
        if 选择 == '1':
            计算器()
        elif 选择 == '2':
            print("再见喵~")
            break
        else:
            print("请输入正确选项~")
            time.sleep(1)

菜单()
